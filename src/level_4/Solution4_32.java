package level_4;

public class Solution4_32 {
    public static void main(String[] args) throws Exception {
        int i = 1;
        while (i<11){
            int k = 1;
            while (k<11){
                System.out.print(i*k+" ");
                k++;
            }
            System.out.println("");
            i++;
        }

    }
}
